from openai import AsyncOpenAI
from app.core.config import settings

client = AsyncOpenAI(api_key=settings.OPENAI_API_KEY)

ENTITY_PROMPT = """Ты — аналитик данных в команде бизнес-аналитиков.
Выяви все сущности, параметры и источники данных из описанного бизнес-процесса.

ПРОЕКТ: {project_name}
ОПИСАНИЕ ПРОЦЕССА И ТРЕБОВАНИЯ:
{context}

ИНСТРУКЦИЯ:
1. СУЩНОСТИ — объекты, которые система создаёт, хранит, обрабатывает.
   Для каждой: название, ключевые атрибуты, тип данных, связи.
2. ПАРАМЕТРЫ ПРОЦЕССА — числовые пороги, статусы, роли, временные ограничения.
3. ИСТОЧНИКИ ДАННЫХ — откуда берётся каждая сущность, кто владелец, system of record.
4. ИНТЕГРАЦИИ — внешние системы, направление передачи данных, триггер.

ФОРМАТ ОТВЕТА (Markdown таблицы):

## Сущности
| Сущность | Атрибуты | Тип | Связи |
|----------|----------|-----|-------|

## Параметры процесса
| Параметр | Значение/диапазон | Где используется |
|----------|--------------------|-----------------|

## Источники данных
| Сущность | Источник | Владелец | Частота обновления |
|----------|----------|----------|--------------------|

## Внешние интеграции
| Система | Что передаётся | Направление | Триггер |
|---------|----------------|-------------|---------|

## Рекомендации
[Что добавить в схему БД, какие справочники завести]"""


async def run_entity_agent(
    context: str,
    project_name: str,
) -> str:

    system = ENTITY_PROMPT.format(
        project_name=project_name,
        context=context,
    )

    response = await client.chat.completions.create(
        model=settings.OPENAI_MODEL,
        max_tokens=settings.OPENAI_MAX_TOKENS,
        temperature=0.2,
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": "Выяви сущности и источники данных по описанию выше."},
        ],
    )

    return response.choices[0].message.content
